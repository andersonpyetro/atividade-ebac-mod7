public class Professor {

    private int codigo;

    private String nome;

    private String cpf;

    public int getCodigo() {
        return codigo;
    }
    public void setCodigo(int codigo) {
        this.codigo = codigo;

    }

    public String getNome(){
        return nome;
    }

    public void setNome (String nome){
        this.nome = nome;
    }

    public String getCpf(){
        return cpf;
    }

    public void getCpf (String cpf){
        this.cpf = cpf;
    }
}

